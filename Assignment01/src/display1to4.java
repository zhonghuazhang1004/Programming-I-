public class display1to4 {
    public static void main(String[] args) {

        System.out.print("1 ");
        System.out.print("2 ");
        System.out.print("3 ");
        System.out.print("4 \n");
        //Use four System.out.print statements
        System.out.println("-----------");
        System.out.println("1 2 3 4");
        //Use one System.out.println statement
        System.out.println("-----------");
        System.out.printf("%d %d %d %d", 1, 2, 3, 4);
        //Use one System.out.printf statement, %d is a placeholder, we can put value in this place later

    }
}
